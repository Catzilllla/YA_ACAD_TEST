
# Dec to Bin
def decimal_to_binary(decimal_num):
    binary_num = ""
    while decimal_num > 0:
        remainder = decimal_num % 2
        binary_num = str(remainder) + binary_num
        decimal_num = decimal_num // 2
    return binary_num

# Пример использования
decimal_num = 10
binary_num = decimal_to_binary(decimal_num)
print("Десятичное число", decimal_num, "в двоичной системе равно", binary_num)

# В этом примере мы определяем функцию `decimal_to_binary`,
# которая принимает десятичное число в качестве аргумента и 
# возвращает его двоичное представление в виде строки.

# Мы используем цикл `while`, чтобы разделить десятичное число на 2
# и сохранить остаток в переменной `remainder`. Затем мы добавляем этот
# остаток в начало строки `binary_num` и делим десятичное число на 2, 
# чтобы продолжить процесс до тех пор, пока десятичное число не станет равным 0.

# Наконец, мы вызываем функцию `decimal_to_binary` с десятичным числом 10 и 
# выводим результат в консоль. Результат будет "Десятичное число 10 в двоичной системе равно 1010".



def binary_to_decimal(binary_num):
    decimal_num = 0
    power = 0
    while binary_num > 0:
        digit = binary_num % 10
        decimal_num += digit * (2 ** power)
        binary_num //= 10
        power += 1
    return decimal_num

# Пример использования
binary_num = 1010
decimal_num = binary_to_decimal(binary_num)
print("Двоичное число", binary_num, "в десятичной системе равно", decimal_num)


# В этом примере мы определяем функцию `binary_to_decimal`, которая принимает двоичное 
# число в качестве аргумента и возвращает его десятичное представление.

# Мы используем цикл `while`, чтобы разделить двоичное число на 10 и сохранить последнюю
# цифру в переменной `digit`. Затем мы добавляем эту цифру, умноженную на 2 в степени `power`, 
# к переменной `decimal_num`. Мы также увеличиваем `power` на 1 и делим двоичное число на 10, 
# чтобы продолжить процесс до тех пор, пока двоичное число не станет равным 0.

# Наконец, мы вызываем функцию `binary_to_decimal` с двоичным числом 1010 и выводим результат 
# в консоль. Результат будет "Двоичное число 1010 в десятичной системе равно 10".